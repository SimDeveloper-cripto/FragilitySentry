package Cascade;

public class Cascade {
    private final int A, B, C;

    public Cascade(int A, int B, int C) {
        this.A = A;
        this.B = B;
        this.C = C;
    }

    public int getA() {
        return A;
    }
    
    public int getB() {
        return B;
    }

    public int getC() {
        return C;
    }
}